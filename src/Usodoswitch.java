public class Usodoswitch {
    public static void main(String[] args) {
        int semana = 1;

        switch (semana){
            case 1:
                System.out.println("Certo");
                break;
            case 2:
                System.out.println("Certo");
                break;
            case 3:
                System.out.println("Certo");
                break;
            case 4:
                System.out.println("Errado");
                break;
            case 5:
                    System.out.println("Talvez");
                    break;
            default:
                    System.out.println("Valor indefinido");

        }
    }
}
